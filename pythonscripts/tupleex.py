#!/usr/bin/python

tupleex = ("python", "tuple", 1952, 2323, 432);

print(tupleex)              # this will print the complete tuple
print(tupleex[1:4])         # this will print the elements starting from 2nd till 4th
print(tupleex[1:])          # this will print the elements starting from the 2nd element
print(tupleex[0])           # this wil print the first element of the tuple
print(tupleex * 2)          # this will print the tuple two times